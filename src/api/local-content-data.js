/**
 * Service de données local pour FloDrama
 */
export class LocalContentService {
  static getInstance() {
    if (!this.instance) {
      this.instance = new LocalContentService();
    }
    return this.instance;
  }
  
  getAllContent() {
    return LOCAL_CONTENT;
  }
  
  getFeaturedContent() {
    return LOCAL_CONTENT.featuredContent;
  }
  
  getDramas() {
    return LOCAL_CONTENT.dramasList;
  }
  
  getMovies() {
    return LOCAL_CONTENT.moviesList;
  }
  
  getAnimes() {
    return LOCAL_CONTENT.animesList;
  }
  
  getContentById(id) {
    // Recherche dans toutes les listes
    const allContent = [
      ...LOCAL_CONTENT.featuredContent,
      ...LOCAL_CONTENT.dramasList,
      ...LOCAL_CONTENT.moviesList,
      ...LOCAL_CONTENT.animesList
    ];
    
    return allContent.find(item => item.id === id);
  }
}

export default LocalContentService.getInstance();
